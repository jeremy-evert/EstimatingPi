public class EstimatingPi {
	public static void main(String[] args) {
		System.out.println("This is an estimate of pi.");
		System.out.println("First Estimate");
		System.out.println(4.0);
		System.out.println("Second Estimate");
		System.out.println(4.0 * (1.0 -(1.0/3)));
		System.out.println("Third Estimate");
		System.out.println(4.0 * (1.0 -(1.0/3)+(1.0/5)));
		

	}

}
